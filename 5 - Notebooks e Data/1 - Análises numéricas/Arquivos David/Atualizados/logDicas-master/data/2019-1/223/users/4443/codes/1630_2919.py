# Entrada das alturas dos integrantes
pri = float(input("Altura do primeiro integrante: "))
seg = float(input("Altura do segundo integrante: "))
ter = float(input("Altura do terceiro integrante: "))
qua = float(input("Altura do quarto integrante: "))

# Calculo da media:
media = (pri + seg + ter + qua)/4

print(round(media, 2))
