from numpy import*
vetor = array(eval(input("Informe o vetor: ")))

print(size(vetor)) # Quantidade de elementos do vetor!
print(vetor[0]) # Primeiro elemento do vetor!
print(vetor[-1]) # Ultimo elemento do vetor"
print(max(vetor)) # Maior elemento do vetor!
print(min(vetor)) # Menor elemento do vetor!
print(sum(vetor)) # Soma dos elementos do vetor!
print(round(mean(vetor),2)) # Média dos elementos do vetor! Não ta dando certo essa bagaça!!!!!