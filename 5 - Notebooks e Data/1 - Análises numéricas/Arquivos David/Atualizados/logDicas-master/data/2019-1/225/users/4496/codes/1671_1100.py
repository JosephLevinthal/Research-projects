
vc = int(input("Digite o valor da cedula: "))

#2 == "Tartaruga"
#5 == "Garça"
#10 == "Arara"
#20 == "Mico-leao-dourado"
#50 == "Onça-pintada"
#100 == "Garoupa"

print ("Entrada: " , vc)
if(vc == 2):
	print("Animal: Tartaruga" )
elif(vc == 5):
	print("Animal: Garca")
elif(vc == 10):
	print ("Animal: Arara")
elif(vc == 20):
	print("Animal: Mico-leao-dourado")
elif(vc == 50):	
	print("Animal: Onca-pintada")
elif(vc == 100):
	print("Animal: Garoupa")
else:
	print("Animal: Invalido")