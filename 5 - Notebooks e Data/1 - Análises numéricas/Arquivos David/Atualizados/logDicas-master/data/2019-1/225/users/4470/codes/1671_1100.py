cedula=int(input("valor de uma cedula:"))
print("Entrada:", cedula)
if(cedula==2):
	print("Animal: Tartaruga")
elif(cedula==5):
	print("Animal: Garca")
elif(cedula==10):
	print("Animal: Arara")
elif(cedula==20):
	print("Animal: Mico-leao-dourado")
elif(cedula==50):
	print("Animal: Onca-pintada")
elif(cedula==100):
	print("Animal: Garoupa")
else:
	print("Animal: Invalido")