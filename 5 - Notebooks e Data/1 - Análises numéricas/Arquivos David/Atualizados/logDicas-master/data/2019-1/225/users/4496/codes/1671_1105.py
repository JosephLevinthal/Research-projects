bra = input("Digite o brasao: ").lower()
print("Entrada:" , bra)
#lobo = Stark #leao = Lannister #veado = Baratheon #dragao = Targaryen
#rosa = Tyrell #sol = Martell #lula = Greyjoy #esfolado = Bolton #turta = Tully

if(bra == "lobo"):
	print("Casa: Stark")
elif(bra == "leao"):
	print("Casa: Lannister")
elif(bra == "veado"):
	print("Casa: Baratheon")
elif(bra == "dragao"):
	print("Casa: Targaryen")
elif(bra == "rosa"):
	print("Casa: Tyrell")
elif(bra == "sol"):
	print("Casa: Martell")
elif(bra == "lula"):
	print("Casa: Greyjoy")
elif(bra == "esfolado"):
	print("Casa: Bolton")
elif(bra == "turta"):	
	print("Casa: Tully")
else:
	print("Brasao invalido")