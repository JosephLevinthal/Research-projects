#cria se a variavel das notas dos marginal
nota1=float(input("nota do marginal1"))
nota2=float(input("nota do marginal2"))
nota3=float(input("nota do marginal3"))
nota4=float(input("nota do marginal4"))
#definise a variavel da media ponderada das notas
media=((nota1*1)+(nota2*2)+(nota3*3)+(nota4*4))/10
mediar=round(media, 2)
print(mediar)

